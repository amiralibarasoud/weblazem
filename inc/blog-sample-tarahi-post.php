function weblazem_seed_tarahi_site_sample_post() {
    if (get_option('weblazem_tarahi_sample_post_seeded') === '1') {
        return;
    }

    $existing = get_page_by_path(WEBLAZEM_TARAHI_SAMPLE_POST_SLUG, OBJECT, 'post');
    if ($existing && $existing->post_status === 'publish') {
        update_option('weblazem_tarahi_sample_post_seeded', '1');
        return;
    }

    $category_name = 'طراحی سایت';
    $category      = get_term_by('name', $category_name, 'category');
    if (!$category) {
        $created = wp_insert_term($category_name, 'category', array(
            'slug' => 'tarahi-site',
        ));
        $cat_id = is_wp_error($created) ? 0 : (int) $created['term_id'];
    } else {
        $cat_id = (int) $category->term_id;
    }

    $post_id = wp_insert_post(array(
        'post_title'   => 'راهنمای کامل طراحی سایت حرفه‌ای؛ از ایده تا راه‌اندازی و سئو',
        'post_name'    => WEBLAZEM_TARAHI_SAMPLE_POST_SLUG,
        'post_excerpt' => weblazem_get_tarahi_site_sample_post_excerpt(),
        'post_content' => weblazem_get_tarahi_site_sample_post_content(),
        'post_status'  => 'publish',
        'post_type'    => 'post',
        'post_date'    => '2026-07-05 09:00:00',
        'comment_status' => 'open',
    ), true);

    if (is_wp_error($post_id) || !$post_id) {
        return;
    }

    if ($cat_id) {
        wp_set_post_categories($post_id, array($cat_id));
    }

    $image_base = get_template_directory_uri() . '/assets/images/blog/';
    update_post_meta($post_id, '_weblazem_demo_thumb', $image_base . 'blog-1.svg');

    update_option('weblazem_tarahi_sample_post_seeded', '1');
}
add_action('init', 'weblazem_seed_tarahi_site_sample_post', 41);
